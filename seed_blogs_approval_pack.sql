Portable air conditioners look simple on paper, but they are one of the easiest appliance categories to buy badly. Shoppers often choose the biggest BTU number they can afford, then end up with a noisy unit, weak airflow, or a machine that is mismatched to the room.

## Start with the Room, Not the Marketing
The first job is to understand the space you want to cool. A small bedroom, a home office with afternoon sun, and a studio apartment kitchen all behave differently. Measure the room and pay attention to ceiling height, direct sunlight, and whether doors stay open during the day.

