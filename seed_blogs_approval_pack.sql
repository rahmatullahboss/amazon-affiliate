Single-hose units are easier to find and usually cheaper. They work, but they often pull conditioned air out of the room while exhausting hot air, which makes them less efficient.

Dual-hose models usually do a better job in hot weather because one hose brings outside air in for the cooling cycle while the other sends hot air back out. If you care about faster cooling and steadier performance, dual-hose is usually the better long-term choice.

